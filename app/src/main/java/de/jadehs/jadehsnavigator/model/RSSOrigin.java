package de.jadehs.jadehsnavigator.model;

public class RSSOrigin {
    private long id;
    private String title;
    private String url;

    public RSSOrigin(long id, String title, String url) {
        this.id = id;
        this.title = title;
        this.url = url;
    }

    public long getID() {
        return id;
    }

    public void setID(long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
